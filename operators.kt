fun main(){
//    Kotlin Operators
//            Operators are used to perform operations on variables and values.
//
//    The value is called an operand, while the operation (to be performed between the two operands) is defined by an operator:
//    Arithmetic Operators
//            Arithmetic operators are used to perform common mathematical operations.
    //+
    //-
    //*
    // /
    // %
    // ++
    // --
    println(10+10)
    println(20-5)
    println(2*10)
    println(100/10)
    println(21%3)
//    Kotlin Assignment Operators
//    Assignment operators are used to assign values to variables.
//
//    In the example below, we use the assignment operator (=) to assign the value 10 to a variable called x:
     // =
     // +=
     // -=
     // *=
     // /=
     // %=
//    Kotlin Comparison Operators
// >
    // <
    // >=
    // <=
    // ==
    // !=
    println(10>5)
    println(10<20)
    println(10>=10)
    println(10<=10)
    println(10==10)
    println(10!=10)
//    Kotlin Logical Operators
//    Logical operators are used to determine the logic between variables or values:
    //&&
    // ||
    // !


    println("true && true => ${true && true}") //true
    println("true && false => ${true && false}")//false
    println("false && false => ${false && false}")//false

    println("true || true => ${true || true}")//true
    println("true || false => ${true || false}")//true
    println("false || false => ${false || false}")//false

    println("!true => ${!true}")//false
    println("!false => ${!false}")//true


}